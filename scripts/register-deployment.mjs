/**
 * scripts/register-deployment.mjs
 *
 * Pre-build script for Vercel deployments.
 * Run via vercel.json buildCommand: "node scripts/register-deployment.mjs && next build"
 *
 * What it does:
 *   1. Inserts a row into public.app_deployments using the service-role client.
 *   2. Gets the auto-incremented version number (id).
 *   3. Writes NEXT_PUBLIC_BUILD_VERSION and NEXT_PUBLIC_BUILD_TIME to .env.local
 *      so next build bakes them into the JS bundle at build time.
 *
 * If any step fails the script exits with code 0 (non-blocking) to avoid
 * failing the entire Vercel build over a non-critical bookkeeping step.
 *
 * Required env vars (all already in Vercel project settings):
 *   NEXT_PUBLIC_SUPABASE_URL    — public Supabase URL
 *   SUPABASE_SERVICE_ROLE_KEY   — service role key (bypasses RLS)
 *
 * Automatically provided by Vercel:
 *   VERCEL_ENV                  — 'production' | 'preview' | 'development'
 *   VERCEL_GIT_COMMIT_SHA       — full commit SHA
 *   VERCEL_GIT_COMMIT_REF       — branch name
 *   VERCEL_GIT_COMMIT_MESSAGE   — commit message
 *   VERCEL_URL                  — deployment URL (no protocol)
 */

import { createClient } from '@supabase/supabase-js';
import { writeFileSync }  from 'fs';

// ── Validate required environment variables ─────────────────────────────────
const supabaseUrl    = process.env.NEXT_PUBLIC_SUPABASE_URL;
const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl || !serviceRoleKey) {
  console.warn('[register-deployment] ⚠️  Missing SUPABASE env vars — skipping deployment registration.');
  process.exit(0);
}

// ── Build metadata from Vercel system env vars ──────────────────────────────
const environment  = process.env.VERCEL_ENV ?? 'development';
const gitShaFull   = process.env.VERCEL_GIT_COMMIT_SHA ?? '';
const gitSha       = gitShaFull.slice(0, 7) || null;
const gitBranch    = process.env.VERCEL_GIT_COMMIT_REF     ?? null;
const gitMessageRaw = process.env.VERCEL_GIT_COMMIT_MESSAGE ?? '';
// Take only the first line of the commit message, max 200 chars
const gitMessage   = gitMessageRaw.split('\n')[0].slice(0, 200) || null;
const vercelUrl    = process.env.VERCEL_URL
  ? `https://${process.env.VERCEL_URL}`
  : null;

const buildTime = new Date().toISOString();

// ── Insert deployment record ────────────────────────────────────────────────
const admin = createClient(supabaseUrl, serviceRoleKey, {
  auth: { autoRefreshToken: false, persistSession: false },
});

const { data, error } = await admin
  .from('app_deployments')
  .insert({
    deployed_at:  buildTime,
    environment,
    git_sha:      gitSha,
    git_branch:   gitBranch,
    git_message:  gitMessage,
    vercel_url:   vercelUrl,
  })
  .select('id')
  .single();

if (error) {
  console.error('[register-deployment] ❌ Failed to register deployment:', error.message);
  // Non-blocking: don't fail the build
  process.exit(0);
}

const version = data.id;
console.log(`[register-deployment] ✓ Version #${version} registered (${environment}) at ${buildTime}`);

// ── Write build-time env vars for Next.js to bake into the bundle ───────────
// .env.local is always read by Next.js regardless of NODE_ENV / VERCEL_ENV.
// We overwrite any existing file so stale version numbers never leak.
const envContent = [
  `# Auto-generated by scripts/register-deployment.mjs — do not edit manually`,
  `NEXT_PUBLIC_BUILD_VERSION=${version}`,
  `NEXT_PUBLIC_BUILD_TIME=${buildTime}`,
  `NEXT_PUBLIC_BUILD_ENVIRONMENT=${environment}`,
  `NEXT_PUBLIC_BUILD_GIT_SHA=${gitSha ?? ''}`,
  `NEXT_PUBLIC_BUILD_GIT_BRANCH=${gitBranch ?? ''}`,
  '',
].join('\n');

try {
  writeFileSync('.env.local', envContent, { encoding: 'utf-8' });
  console.log('[register-deployment] ✓ .env.local written with build metadata');
} catch (writeErr) {
  console.warn('[register-deployment] ⚠️  Could not write .env.local:', writeErr.message);
}
